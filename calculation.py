var1 = 3
var2 = 6

# Addition operator
print(var1 + var2)
print(5 + 7)

# Substraction operator
print(var2 - var1)
print(9 - 8)

# Multiplication operator
print(var1 * var2)
print(4 * 6)

# Division operator
print(var2 / var1)
print(9 / 3)
print(10 / 3)

# The order of the operations
print(5 + 2 * 3 / 2)
print(12 - 6 / 2)

